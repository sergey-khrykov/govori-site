function setLang(lang) {
  const t = translations[lang];
  if (!t) return;

  // Update all data-i18n text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] != null) el.textContent = t[key];
  });

  // Update all data-i18n-html nodes (allow HTML content)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] != null) el.innerHTML = t[key];
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] != null) el.placeholder = t[key];
  });

  // Update page title and meta
  document.title = t.title || document.title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && t.description) metaDesc.content = t.description;

  // Update html lang
  document.documentElement.lang = t.lang || lang;

  // Mark active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Persist choice
  localStorage.setItem('govori-lang', lang);

  // Swap video sources based on language
  const videoFolder = lang === 'ru' ? 'video/ru/' : 'video/en/';
  const ruVideos = new Set([
    'feat1_latin_zena.mp4', 'feat1_cyrillic_zena.mp4', 'feat1_no_diacritics.mp4',
    'feat2_form_ljudi.mp4', 'feat2_form_zavolela.mp4', 'feat2_translation_great.mp4',
    'feat3_verb_studirati.mp4', 'feat3_adjective.mp4', 'feat4_labels_bread.mp4',
    'feat6_recents_favorites.mp4', 'feat7_change_scripts.mp4'
  ]);

  document.querySelectorAll('.feature-media video').forEach(video => {
    const source = video.querySelector('source');
    if (!source) return;
    const src = source.getAttribute('src');
    const filename = src.split('/').pop();
    const wrap = video.closest('.video-wrap');

    if (lang === 'ru' && !ruVideos.has(filename)) {
      if (wrap) wrap.style.display = 'none';
      return;
    }
    if (wrap) wrap.style.display = '';
    const newSrc = videoFolder + filename;
    if (src !== newSrc) {
      source.setAttribute('src', newSrc);
      video.load();
    }
  });

  // Show/hide carousel dots to match visible videos
  document.querySelectorAll('.video-carousel').forEach(carousel => {
    const dots = carousel.querySelectorAll('.carousel-dot');
    const wraps = carousel.querySelectorAll('.video-wrap');
    wraps.forEach((wrap, i) => {
      if (dots[i]) dots[i].style.display = wrap.style.display;
    });
  });
}

function initI18n() {
  // Detect language: URL param > localStorage > browser
  const params = new URLSearchParams(window.location.search);
  let lang = params.get('lang');
  if (!lang) lang = localStorage.getItem('govori-lang');
  if (!lang) {
    const bl = navigator.language.toLowerCase();
    if (bl.startsWith('ru')) lang = 'ru';
    else if (bl === 'sr' || bl.startsWith('sr-cyrl')) lang = 'sr-cyrl';
    else if (bl.startsWith('sr')) lang = 'sr-latn';
    else lang = 'en';
  }
  if (!translations[lang]) lang = 'en';

  // Bind switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      setLang(btn.getAttribute('data-lang'));
    });
  });

  setLang(lang);
}

document.addEventListener('DOMContentLoaded', initI18n);
